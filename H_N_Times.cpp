#include <bits/stdc++.h>
using namespace std;
int main()
{
    int t;
    cin >> t;
    int l = 0;
    while (t > l)
    {
        int n;
        cin >> n;
        char c;
        cin >> c;
        for (int i = 0; i < n; i++)
        {
            cout << c << " ";
        }
        cout<<endl;
       
       t--;
    }
     
}